package core;

/**
* Interface with tiny integer number constants declarations for the values of <code>trinary</code> valuespace (<code>yes</code>, <code>no</code> and <code>maybe</code>).<br>
*
* @author Marc E. KAMGA
* @version 1.0
* @copyright Marc E. KAMGA
*/
public interface ITrinaryValues {

	/**
	* Constant for value <code>yes</code> from <code>trinary</code> valuespace.
	*/
	public static final byte yes = 1;
	/**
	* Constant for value <code>no</code> from <code>trinary</code> valuespace.
	*/
	public static final byte no = -1;
	/**
	* Constant for value <code>maybe</code> from <code>trinary</code> valuespace.
	*/
	public static final byte maybe = 0;


}